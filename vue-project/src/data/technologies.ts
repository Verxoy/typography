export const TECH_SIDEBAR_ITEMS = TECHNOLOGY_SECTIONS.map((s) => s.title)

/** Преобразует пункты с абзацами в формат подразделов (единый UI). */
export function paragraphItemsToSubs(
  items: { id: string; title: string; paragraphs: string[] }[],
  itemsIntro?: string,
): TechSubs {
  return {
    intro: [],
    itemsIntro,
    items: items.map((item) => ({
      id: item.id,
      title: item.title,
      blocks: item.paragraphs.map((text) => ({ type: 'paragraph' as const, text })),
    })),
  }
}
